package kosta.finalproject.customer.dto;

public class CustomersTDTO {
	private String c_id;
	private String c_pw;
	private String c_name;
	private String c_email;
	private int c_coin;
	private int c_birth;
	
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public String getC_pw() {
		return c_pw;
	}
	public void setC_pw(String c_pw) {
		this.c_pw = c_pw;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_email() {
		return c_email;
	}
	public void setC_email(String c_email) {
		this.c_email = c_email;
	}
	public int getC_coin() {
		return c_coin;
	}
	public void setC_coin(int c_coin) {
		this.c_coin = c_coin;
	}
	public int getC_birth() {
		return c_birth;
	}
	public void setC_birth(int c_birth) {
		this.c_birth = c_birth;
	}
	
	
	
}
